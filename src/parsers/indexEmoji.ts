
function indexToEmoji(index: number): string {
    const emojis = [
        ':one:',
        ':two:',
        ':three:',
        ':four:',
        ':five:',
        ':six:',
        ':seven:',
        ':eight:',
        ':nine:',
        ':ten:'
    ]
    return emojis[index];
}

export { indexToEmoji };